function showDiary() {
    var x = document.getElementById('showDiary');
    if(x.style.display === 'none'){
        x.style.display = 'block';
    } else {
        x.style.display = 'none';
    }
}

function showRanking() {
    var x = document.getElementById('ranking');
    if(x.style.display === 'none'){
        x.style.display = 'block';
    } else {
        x.style.display = 'none';
    }
}


